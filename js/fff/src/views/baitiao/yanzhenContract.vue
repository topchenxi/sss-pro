      let lT = new Date(this.loanTime).getTime()
      let lY = new Date(lT).getFullYear()
      let lM = new Date(lT).getMonth() + 1
      let lD = new Date(lT).getDate()
      let eT = new Date(this.expectedRepaymentTime).getTime()
      let eY = new Date(eT).getFullYear()
      let eM = new Date(eT).getMonth() + 1
